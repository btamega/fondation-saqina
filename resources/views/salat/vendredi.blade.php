                        </li>
                        <li>en arabe.</li>
                        <li>A voix haute.</li>
                        <li>après le zénith et avant la prière.</li>
                        <li>se composer de 2 parties bien distinctes en séparant la première de la deuxième par
                            un temps, et que l’imam s’assoit entre les 2.
                        </li>
                        <li>Qu’elle se fasse immédiatement suivre par la salat du vendredi.</li>
                        <li>l faut qu’il y ait au cours de la khotba une assemblée constituée par des personnes
