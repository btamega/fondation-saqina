                        <ul>
                            <li>Il faut pour cet Imam, que cette prière lui soit obligatoire, c’est-à-dire qu’il ne faut pas que ce
                                soit un enfant ou un voyageur.</li>
                                <li>Il faut que celui qui fasse la prière du vendredi soit également celui qui fait la khotba (le
